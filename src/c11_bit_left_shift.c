/*
 * Example #11
 * Bit left shift.
 */
#include <stdio.h>

int main(void)
{
    printf("1 << 0 = %d\n",  1 << 0);
    printf("1 << 1 = %d\n",  1 << 1);
    printf("1 << 2 = %d\n",  1 << 2);
    printf("1 << 3 = %d\n",  1 << 3);
    printf("1 << 4 = %d\n",  1 << 4);
    printf("1 << 10 = %d\n", 1 << 10);
    printf("1 << 20 = %d\n", 1 << 20);
    printf("\n");

    printf("3 << 0 = %d\n",  3 << 0);
    printf("3 << 1 = %d\n",  3 << 1);
    printf("3 << 2 = %d\n",  3 << 2);
    printf("3 << 3 = %d\n",  3 << 3);
    printf("3 << 4 = %d\n",  3 << 4);
    printf("3 << 10 = %d\n", 3 << 10);
    printf("3 << 20 = %d\n", 3 << 20);
    printf("\n");

    return 0;
}

