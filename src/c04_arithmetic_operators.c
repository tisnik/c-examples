/*
 * Example #4
 * Arithmetic operators basic usage.
 */
#include <stdio.h>

int main(void)
{
    printf("Positive operands:\n");
    printf("%d\n", 10 + 3);
    printf("%d\n", 10 - 3);
    printf("%d\n", 10 * 3);
    printf("%d\n", 10 / 3);
    printf("%d\n", 10 % 3);
    printf("\nNegative operands:\n");
    printf("%d\n", -10 + -3);
    printf("%d\n", -10 - -3);
    printf("%d\n", -10 * -3);
    printf("%d\n", -10 / -3);
    printf("%d\n", -10 % -3);
    return 0;
}

