/*
 * Example #6
 * Arithmetic operators basic usage.
 */
#include <stdio.h>

int main(void)
{
    printf("Positive operands:\n");
    printf("%f\n", 10.0 + 3);
    printf("%f\n", 10.0 - 3);
    printf("%f\n", 10.0 * 3);
    printf("%f\n", 10.0 / 3);
    printf("\nNegative operands:\n");
    printf("%f\n", -10.0 + -3);
    printf("%f\n", -10.0 - -3);
    printf("%f\n", -10.0 * -3);
    printf("%f\n", -10.0 / -3);
    return 0;
}

