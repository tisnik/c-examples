/*
 * Example #5
 * Arithmetic operators basic usage.
 */
#include <stdio.h>

int main(void)
{
    int x = 10;
    int y = 10;
    printf("-- operator:\n");
    printf("original x value = %d\n", x);
    printf("original y value = %d\n", y);
    printf("x--              = %d\n", x--);
    printf("--y              = %d\n", --y);
    printf("new x value      = %d\n", x);
    printf("new y value      = %d\n", y);
    return 0;
}

